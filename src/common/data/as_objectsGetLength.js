/**
 * 
 * @description
 * @param obj {Object}
 */
function as_objectsGetLength(obj) {
	var len = 0, key;
	for (key in obj) {
		if (obj.hasOwnProperty(key)) {
			len++;
		}
	}
	return len;
}
